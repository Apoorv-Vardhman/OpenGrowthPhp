                    <p class="mt-3">Face ID will work while wearing a mask. Do you find it funny? No way! I am not joking. It is true! With iOS 15.4, you can now unlock your iPhone’s Face ID even while wearing a mask. However, the competence is limited to the iPhone 12 and newer devices. The company has also updated the long-awaited Universal Control. Let’s understand a bit about the new feature, Universal Control, and how to use the unlock feature on the iPhones.</p>
                    <h6 class="mt-4">Statement from Apple</h6>
                    <p>According to Apple, “ The feature scans unique features around the eye area to be authentic,” though it does warn that Face ID works best when the entire face is visible. However, users do not need to wear a mask while setting up Face ID.</p>
                    <p class="mt-3">Not only facial recognition with a mask, but Apple is also improving Face ID with glasses. The company further explains, “ Using Face ID while wearing a mask works best when it’s set up to recognize each pair of glasses you wear regularly.” Face ID with mask doesn’t support sunglasses.</p>
                    <p class="mt-3">The company also says, “ Face ID is most accurate when it’s set up for full-face recognition only. To use Face ID while wearing a mask,iPhone can recognize the unique features around the eye to authenticate.”</p>
                    <h6 class="mt-4">How to Set up Face ID</h6>
                    <p>Here are the steps you must follow to set up a face ID on Apple iPhone to work with face masks. Remember, the update is around 12.5 GB, so make sure you have access to Wifi while installing.</p>
                    <ul class="ml-5">
                        <li>Make sure your iPhone has iOS 15.4</li>
                        <li>Open settings, tap Face ID and Password</li>
                        <li>Scroll down and turn Face ID with a mask</li>
                        <li>Tab Use Face ID with a Mask Follow the on-screen instructions to set up a face ID</li>
                    </ul>
                    <img src="assets/images/blogs/banner-2.png">
                    <h6 class="mt-4">What does iOS 15.4 Update Bring?</h6>
                    <p class="mt-3">The update does not only bring the feature of face ID, but it comes with a lot more. Don’t worry, I am here to tell you. </p>
                    <h6 class="mt-4">Face ID</h6>
                    <p class="mt-3">As I told you already, the new feature has the option of Face ID that you can access by updating your iPhone. You will also be able to access Apple Pay and password autofill with your Face ID while </p>
                    <h6 class="mt-4">wearing a mask</h6>
                    <p class="mt-3">The capability is limited to newer devices, which include the iPhone 12, iPhone 12 mini,iPhone 12 Pro, Pro Max, and the latest iPhone 13 lineup. The remaining users have to remove their masks to unlock the device.</p>
                    <h6 class="mt-4">Emoji</h6>
                    <p class="mt-3">Emoji The update also brings new emoticons that include face gestures, hand gestures, and household objects to the keyboard. Moreover, the handshake emoji is now available in separate skin tones for each hand.</p>
                    <h6 class="mt-4">Siri</h6>
                    <p class="mt-3">Siri is the best friend of iPhone users! Siri can now provide time and date information while offline on iPhone XS, iPhone XR, iPhone 11, and the newer iPhones.</p>
                    <h6 class="mt-4">Vaccination Cards</h6>
                    <p class="mt-3">COVID-19 has made it compulsory to show your vaccination certificate everywhere. The Digital Covid Certificate support in the Health app enables you to download and store verifiable versions of your COVID-19 vaccination, test results, and recovery records. The vaccination cards in Apple Wallet will now sport the EU COVID Certificate format.</p>

                    <h6 class="mt-4">Other Features</h6>
                    <p class="mt-3">The other features include support for Italian and Chinese in Safari web page translation and FaceTime updates. Even the Podcasts app now adds episode filters for seasons as played, unplayed, saved and downloaded episodes. Moreover, the shortcuts now support adding, removing, and querying tags with reminders.</p>
                    <p class="mt-3">The update has also fixed issues like the Speak Screen Accessibility feature quitting unexpectedly within the Book app and the Live Listen feature not turning off when switched off in the Control Center.</p>

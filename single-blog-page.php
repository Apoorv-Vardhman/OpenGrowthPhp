                    <h6 class="mt-4">What is Universal Control?</h6>
                    <p class="mt-3">Universal Control is a feature in the iPhone that lets you use other Apple devices as a second screen. The feature was initially announced as part of macOS Monetary but was delayed. By the time, it became part of the iOS 15.4 beta and launched as a public beta, with the Monterey OS 12.4 and iPadOS 15.4 updates.</p>
                    <p class="mt-3">As Universal Control is still in beta, it is expected to run with some issues. According to Apple, Universal Control is available on:</p>
